    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        ArrayList<Insurance> insuranceList = insuranceOrganization.getInsuranceList();
        String search = txtSearch.getText().trim();
        
        if (insuranceOrganization.getInsuranceDirectory().searchInsuranceList(search) == null
                || txtSearch.getText().isBlank()){
            JOptionPane.showMessageDialog(null, "Product not found", "Information", JOptionPane.INFORMATION_MESSAGE);
            txtSearch.setText("");
            return;
        }
        ArrayList<Insurance> searchInsurance = insuranceOrganization.getInsuranceDirectory().searchInsuranceList(search);
        
        DefaultTableModel model = (DefaultTableModel) tblins.getModel();
        model.setRowCount(0);
        
        for(Insurance i: searchInsurance){
            Object row[] = new Object[5];
            row[0] = i;
            row[1] = i.getInsName();
            row[2] = i.getInsType();
            row[3] = i.getInsHospital();
            row[4] = i.getInsCharge();
            model.addRow(row);
        }
        
        txtSearch.setText("");
    }//GEN-LAST:event_btnSearchActionPerformed


